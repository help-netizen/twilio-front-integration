#!/usr/bin/env node
/**
 * gen-login-history.mjs
 * ---------------------------------------------------------------------------
 * Generates the "Shipped recently" feed for the Albusto Keycloak login theme
 * from git history. Output:
 *   keycloak-themes/albusto/login/history.ftl   (FreeMarker include)
 *
 * Run locally (the prod box has no .git):
 *   node scripts/gen-login-history.mjs
 *   npm run gen:login-history
 *
 * Only user-facing commits are shown (feat / fix / perf / redesign / polish …);
 * chore/docs/test/ci/build/merge noise is dropped, conventional-commit prefixes
 * are stripped, and the whole include is wrapped in <#noparse> so a stray
 * "${" or "<#" in a commit message can never break template rendering.
 *
 * CURATION: auto-derived text is only as good as the commit subject. To polish a
 * line (or hide/retag it) durably — without hand-editing this generated file —
 * add an entry to scripts/login-history-overrides.json keyed by the short sha:
 *   { "8611665": { "tag": "Feature", "text": "Friendly, user-facing description" },
 *     "abc1234": { "hide": true } }
 * Overrides survive regeneration. Re-run `npm run gen:login-history` after editing.
 */
import { execSync } from 'node:child_process';
import { writeFileSync, mkdirSync, readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = resolve(ROOT, 'keycloak-themes/albusto/login/history.ftl');
const MAX_ITEMS = 40;
const US = '\x1f'; // unit separator between fields
const RS = '\x1e'; // record separator between commits

const SKIP = new Set(['chore', 'docs', 'doc', 'test', 'tests', 'ci', 'build', 'deps', 'dep', 'wip', 'revert']);

// Curated overrides (sha → { text?, tag?, hide? }) — polish/hide/retag durably.
let OVERRIDES = {};
try {
  OVERRIDES = JSON.parse(readFileSync(resolve(ROOT, 'scripts/login-history-overrides.json'), 'utf8'));
} catch { /* no overrides file — fine */ }

function tagToCat(tag) {
  const t = String(tag).toLowerCase();
  return t === 'feature' ? 'feature' : t === 'fix' ? 'fix' : 'polish';
}

function esc(s) {
  return String(s)
    .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function classify(subject) {
  if (/^merge\b/i.test(subject)) return null;
  const m = subject.match(/^([a-zA-Z]+)(\([^)]*\))?(!)?:\s*(.+)$/);
  let cat = 'polish', tag = 'Update', text = subject;
  if (m) {
    const type = m[1].toLowerCase();
    if (SKIP.has(type)) return null;
    text = m[4];
    if (['feat', 'feature'].includes(type)) { cat = 'feature'; tag = 'Feature'; }
    else if (['fix', 'hotfix', 'bugfix'].includes(type)) { cat = 'fix'; tag = 'Fix'; }
    else { cat = 'polish'; tag = 'Polish'; } // perf / refactor / redesign / polish / ui / style / design …
  }
  // tidy the human text
  text = text.replace(/\s*\(#\d+\)\s*$/, '');                                   // drop trailing (#123)
  text = text.replace(/\s*\(([A-Z][A-Z0-9]*(?:-[A-Z0-9]+)+)\)\s*$/, '');        // drop trailing (MODAL-REDESIGN-001)
  text = text.replace(/\s*[—–-]\s*[A-Z][A-Z0-9]*(?:-[A-Z0-9]+)+\s*$/, '');      // drop trailing — JOB-CREATE-001
  text = text.replace(/^[A-Z][A-Z0-9]*(?:-[A-Z0-9]+)+(?:\s+[—–-]\s+|\s+)/, ''); // drop leading EMAIL-TIMELINE-001 — / REC-SETTINGS-001
  text = text.replace(/\s+/g, ' ').trim();
  if (text) text = text[0].toUpperCase() + text.slice(1);
  return { cat, tag, text };
}

function fmtDate(iso) {
  const d = new Date(iso);
  return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' });
}
function dayKey(iso) { return iso.slice(0, 10); }

// ── read history ────────────────────────────────────────────────────────────
let raw = '';
try {
  raw = execSync(
    `git log -n 250 --no-merges --pretty=format:"%h${US}%cI${US}%s${RS}"`,
    { cwd: ROOT, encoding: 'utf8', maxBuffer: 8 * 1024 * 1024 },
  );
} catch (e) {
  console.error('git log failed:', e.message);
  process.exit(1);
}

const items = [];
for (const rec of raw.split(RS)) {
  const line = rec.trim();
  if (!line) continue;
  const [sha, iso, subject] = line.split(US);
  if (!sha || !iso || !subject) continue;
  const c = classify(subject);
  if (!c) continue;
  const ov = OVERRIDES[sha];
  if (ov) {
    if (ov.hide) continue;
    if (ov.text) c.text = ov.text;
    if (ov.tag) { c.tag = ov.tag; c.cat = tagToCat(ov.tag); }
  }
  items.push({ sha, iso, ...c });
  if (items.length >= MAX_ITEMS) break;
}

// ── group by day, build FTL ──────────────────────────────────────────────────
const out = [];
out.push('<#-- AUTO-GENERATED by scripts/gen-login-history.mjs — do not edit by hand. -->');
out.push('<#noparse>');

let curDay = null;
for (const it of items) {
  const k = dayKey(it.iso);
  if (k !== curDay) {
    curDay = k;
    out.push(`<div class="day"><span class="day__date">${esc(fmtDate(it.iso))}</span><span class="day__line"></span></div>`);
  }
  const bulletMod = it.cat === 'fix' ? ' item__bullet--fix' : it.cat === 'polish' ? ' item__bullet--polish' : '';
  const tagMod = it.cat === 'fix' ? ' item__tag--fix' : it.cat === 'polish' ? ' item__tag--polish' : '';
  out.push(
    `<div class="item">` +
    `<div class="item__rail"><span class="item__bullet${bulletMod}"></span></div>` +
    `<div class="item__body">` +
    `<span class="item__tag${tagMod}">${esc(it.tag)}</span>` +
    `<div class="item__text">${esc(it.text)}<span class="item__sha">${esc(it.sha)}</span></div>` +
    `</div></div>`,
  );
}
if (!items.length) out.push('<div class="news-empty">Release history will appear here.</div>');
out.push('</#noparse>');
out.push('');

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, out.join('\n'), 'utf8');
console.log(`✓ wrote ${items.length} items → ${OUT.replace(ROOT + '/', '')}`);
